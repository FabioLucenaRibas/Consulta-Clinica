public final class PlanodeSaude {
    private String codPlano;
    private String nomePlano;    

    /**
     * @return the codPlano
     */
    public String getCodPlano() {
        return codPlano;
    }

    /**
     * @param codPlano the codPlano to set
     */
    public void setCodPlano(String codPlano) {
        this.codPlano = codPlano;
    }

    /**
     * @return the nomePlano
     */
    public String getNomePlano() {
        return nomePlano;
    }

    /**
     * @param nomePlano the nomePlano to set
     */
    public void setNomePlano(String nomePlano) {
        this.nomePlano = nomePlano;
    }

}
