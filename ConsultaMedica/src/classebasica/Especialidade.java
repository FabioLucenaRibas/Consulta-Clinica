package classebasica;

/**
 *
 * @author MegaKingBR
 */
public final class Especialidade {
    private String codEspecialidade;
    private String nomeEspecialidade;   

    /**
     * @return the codEspecialidade
     */
    public String getCodEspecialidade() {
        return codEspecialidade;
    }

    /**
     * @param codEspecialidade the codEspecialidade to set
     */
    public void setCodEspecialidade(String codEspecialidade) {
        this.codEspecialidade = codEspecialidade;
    }

    /**
     * @return the nomeEspecialidade
     */
    public String getNomeEspecialidade() {
        return nomeEspecialidade;
    }

    /**
     * @param nomeEspecialidade the nomeEspecialidade to set
     */
    public void setNomeEspecialidade(String nomeEspecialidade) {
        this.nomeEspecialidade = nomeEspecialidade;
    }
}
