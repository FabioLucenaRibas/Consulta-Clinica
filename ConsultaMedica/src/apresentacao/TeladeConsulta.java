    private void jButtonRemoverProntuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverProntuarioActionPerformed
    try {
        int resp = JOptionPane.showConfirmDialog(null, "Voce deseja remover o prontuario?", "Remover Prontúario", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
        c.setNumeroProntuario(jTextFieldProntuario.getText());
        NegocioTeladeConsulta ntc = new NegocioTeladeConsulta();
        ntc.remover(c);
        LimparTela();
        ListarProntuario();
        jTextFieldProntuario.setText("");
    }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
    }
    }//GEN-LAST:event_jButtonRemoverProntuarioActionPerformed

