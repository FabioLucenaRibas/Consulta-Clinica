    private void jButtonAtualizarProntuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarProntuarioActionPerformed
    try {
        int resp = JOptionPane.showConfirmDialog(null, "Voce deseja atualizar o prontuario?", "Atualizar Prontúario", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
        NegocioTeladeConsulta ntc = new NegocioTeladeConsulta();
            c.getPaciente().setCpf_Paciente(jTextFieldCpfPaciente.getText());
            c.getMedico().setCrm_Medico(jTextFieldCrmMedico.getText());
            c.setDataConsulta(jFormattedTextFieldDataConsulta.getText());
            c.setHoraInicio(jFormattedTextFieldHoraInicio.getText());
            c.setHoraFinal(jFormattedTextFieldHoraFinal.getText());
            c.setDescricaoConsulta(jTextAreaRelatorio.getText()); 
        ntc.atualizar(c);
        ListarMedico();
    }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
    }
    }//GEN-LAST:event_jButtonAtualizarProntuarioActionPerformed

