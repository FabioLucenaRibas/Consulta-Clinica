    private void ListarProntuario() {
        try {
            NegocioTeladeConsulta ntc = new NegocioTeladeConsulta();
            listaprontuario = ntc.listarProntuario();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"Numero do Prontúario", "CPF do paciente", "Nome do paciente", "Plano de Saúde", "Nome do Medico", "Especialidade","Data da consulta", "Hora Inicio", "Hora Termino"});
            for (Consulta c : listaprontuario) {
                modelo.addRow(new String[]{c.getNumeroProntuario() + "", c.getPaciente().getCpf_Paciente() + "", c.getPaciente().getNome() + "", c.getPaciente().getPlanodeSaude().getNomePlano() + "", 
                c.getMedico().getNome() + "", c.getMedico().getEspecialidade().getNomeEspecialidade() + "", c.getDataConsulta()+ "", c.getHoraInicio() + "", c.getHoraFinal()});
            }
            jTableListagem.setModel(modelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }    
    }
    private void ListarMedico() {
        try {
            NegocioRegistroMedico nrm = new NegocioRegistroMedico();    
            ArrayList<Medico> listamedico = nrm.listarmedico();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"CRM", "Nome", "Sexo", "Data de Nascimento", "Especialidade"});
            for (Medico m : listamedico) {
                modelo.addRow(new String[]{m.getCrm_Medico() + "", m.getNome() + "", m.getSexo().getNome_Sexo() + "", m.getDataNasimento() + "", m.getEspecialidade().getNomeEspecialidade()});
            }
            jTableListagem.setModel(modelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
    private void ListarPaciente() {
        try {
        NegocioRegistroPaciente nrp = new NegocioRegistroPaciente();
            ArrayList<Paciente> listap = nrp.listarPaciente();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"CPF", "Nome", "Sexo", "Data de Nascimento", "Plano de Saúde"});
            for (Paciente p : listap) {
                modelo.addRow(new String[]{p.getCpf_Paciente() + "", p.getNome() + "", p.getSexo().getNome_Sexo() + "", p.getDataNasimento() + "", p.getPlanodeSaude().getNomePlano()});
            }
            jTableListagem.setModel(modelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
