    private void ListarPaciente() {
        try {
            // TODO add your handling code here:
            ArrayList<Paciente> listap = nrp.listarPaciente();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"CPF", "Nome", "Sexo", "Data de Nascimento", "Plano de Sa√∫de"});
            for (Paciente p : listap) {
                modelo.addRow(new String[]{p.getCpf_Paciente() + "", p.getNome() + "", p.getSexo().getNome_Sexo() + "", p.getDataNasimento() + "", p.getPlanodeSaude().getNomePlano()});
            }
            jTableTabelaPaciente.setModel(modelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
