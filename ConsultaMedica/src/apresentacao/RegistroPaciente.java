    private void mostrarPlanodesaudeComboBox() {
        try {
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
                ArrayList<Paciente> listPlanodesaude = nrp.listPlanodeSaude();
            for (Paciente p : listPlanodesaude) {
                modelo.addElement(p.getPlanodeSaude().getNomePlano());
            }
            jComboBoxPlanoPaciente.setModel(modelo);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    private void mostrarSexoComboBox() {
        try {
            listSexo = nrp.listarsexo();
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
            for (int i = 0; i < listSexo.size(); i++) {
                Paciente p = listSexo.get(i);
                modelo.addElement(p.getSexo().getNome_Sexo());
            }
            jComboBoxSexoPaciente.setModel(modelo);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
